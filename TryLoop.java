public class TryLoop {
    public static void main(String[] args) {
        

        for(int j = 5; j > 0; j--){
            System.out.println("");
            for(int i = j; i > 0; i--){
                System.out.print(" *");
            }
        }
        System.out.println("");
        
        
        
    }
    
}
